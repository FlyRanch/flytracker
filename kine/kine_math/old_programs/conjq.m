% Conjugate of the quaternion
%
% q_in is a quaternion (4x1 or 1x4 vector).
% The output, q_out, is the conjugate, also a quaternion.
% If q_in is a unit quaternion, then the conjugate quaternion represents a
% rotation around the same axis but in the opposite direction.

function q_out = conjq(q_in)
q_out = [q_in(1) -q_in(2) -q_in(3) -q_in(4)];
